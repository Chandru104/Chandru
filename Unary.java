class Unary 
{
	public static void main(String[] args) 
	{
		int num1=0;
		int num2=1;
		System.out.println("Post Increment : "+ num1++);
		System.out.println("Pre Increment : "+ ++num1);
		System.out.println("**************************");
		System.out.println("Post Decrement : "+ num2--);
		System.out.println("Pre Decrement : "+ --num2);
	}
}